import Fluent
import Vapor
import SharedModels

/// Fluent database model for a User.
final class UserModel: Model, Content, @unchecked Sendable {
    static let schema = "users"

    @ID(key: .id)
    var id: UUID?

    @Field(key: "email")
    var email: String

    @Field(key: "display_name")
    var displayName: String

    @Field(key: "password_hash")
    var passwordHash: String

    @Enum(key: "role")
    var role: UserRole

    @Timestamp(key: "created_at", on: .create)
    var createdAt: Date?

    @Timestamp(key: "updated_at", on: .update)
    var updatedAt: Date?

    @Children(for: \.$assignee)
    var assignedTasks: [TaskItemModel]

    init() {}

    init(
        id: UUID? = nil,
        email: String,
        displayName: String,
        passwordHash: String,
        role: UserRole = .member
    ) {
        self.id = id
        self.email = email
        self.displayName = displayName
        self.passwordHash = passwordHash
        self.role = role
    }

    /// Convert to the shared DTO for API responses.
    func toDTO() -> UserDTO {
        UserDTO(
            id: id ?? UUID(),
            email: email,
            displayName: displayName,
            role: role,
            createdAt: createdAt,
            updatedAt: updatedAt
        )
    }
}
